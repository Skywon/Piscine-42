char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (j < nb && src[j] != '\0')
	{
		dest[i + 1] = src[j];
		i++;
		j++;
	}
	dest[i + 1] = '\0';
	return (dest);
}
