int		ft_atoi(char *str)
{
	int res;
	int sign;
	int q;

	res = 0;
	sign = 1;
	q = 0;
	while ((str[q] > 9 && str[q] < 13) || str[q] == ' ')
		q++;
	if (str[q] == 43 && (str[q + 1] >= 48 && str[q + 1] <= 57))
		q++;
	if (str[q] == 45 && (str[q + 1] >= 48 && str[q + 1] <= 57))
	{
		sign = -1;
		q++;
	}
	while (str[q] != '\0')
	{
		if (str[q] < '0' || str[q] > '9')
			break ;
		res = res * 10 + str[q] - '0';
		q++;
	}
	return (sign * res);
}
