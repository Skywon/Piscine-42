int	ft_any(int **tab, void(*f)(int))
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (tab[j][i] =! 0)
	{
		while (tab[j][i] =! '0')
		{
			f(i);
			if (f(i) = 1)
				return (1);
			i++;
		}
		j++;
	}
	return (0);
}
