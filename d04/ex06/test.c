int 	ft_is_prime(int nb)
{
	int j;
	int div;

	div = 0;
	while (j <= nb)
	{
		if (nb % j == 0)
			div++;
		j++;
	}
	if (nb == 0 || nb == 1)
		return (0);
	if (div == 2)
		return (1);
	else 
		return (0);
}
